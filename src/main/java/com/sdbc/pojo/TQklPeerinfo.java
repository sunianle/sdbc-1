package com.sdbc.pojo;

public class TQklPeerinfo {
    private String peerUuid;

    private String peerId;

    private String peerName;

    private String peerType;

    private String peerPkiid;

    private String peerDesc;

    private String peerAddress;

    public String getPeerUuid() {
        return peerUuid;
    }

    public void setPeerUuid(String peerUuid) {
        this.peerUuid = peerUuid;
    }

    public String getPeerId() {
        return peerId;
    }

    public void setPeerId(String peerId) {
        this.peerId = peerId;
    }

    public String getPeerName() {
        return peerName;
    }

    public void setPeerName(String peerName) {
        this.peerName = peerName;
    }

    public String getPeerType() {
        return peerType;
    }

    public void setPeerType(String peerType) {
        this.peerType = peerType;
    }

    public String getPeerPkiid() {
        return peerPkiid;
    }

    public void setPeerPkiid(String peerPkiid) {
        this.peerPkiid = peerPkiid;
    }

    public String getPeerDesc() {
        return peerDesc;
    }

    public void setPeerDesc(String peerDesc) {
        this.peerDesc = peerDesc;
    }

    public String getPeerAddress() {
        return peerAddress;
    }

    public void setPeerAddress(String peerAddress) {
        this.peerAddress = peerAddress;
    }
}